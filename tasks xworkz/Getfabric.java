public class Getfabric {
    static int price(String fabric){
        if(fabric=="cotton") return 100;
        if(fabric=="nylon") return 200;
        if(fabric=="woolen") return 300;
        if(fabric=="linen") return 400;
        if(fabric=="polyster") return 500;
        if(fabric=="silk") return 600;
        else
        return 0;
    }
}
